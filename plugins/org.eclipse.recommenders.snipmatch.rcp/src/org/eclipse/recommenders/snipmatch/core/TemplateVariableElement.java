package org.eclipse.recommenders.snipmatch.core;

public class TemplateVariableElement {
    private int start;
    private int length;
    private String value;

    public TemplateVariableElement(int start, int length, String value) {
        this.start = start;
        this.length = length;
        this.value = value;
    }

     public int getStart() {
        return start;
    }

    public void setStart(int start) {
        this.start = start;
    }

    public int getLength() {
        return length;
    }

    public void setLength(int length) {
        this.length = length;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }
}
